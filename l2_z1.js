//Вывести индекс минимального элемента. Найдите индекс минимального элемента в массиве. 
let arr = [5, 3, 8, 1, 4]; // пример массива
let minIndex = 0; // предполагаем, что минимальный элемент - первый

for (let i = 1; i < arr.length; i++) {
  if (arr[i] < arr[minIndex]) {
    minIndex = i; // обновляем индекс, если находим элемент меньше текущего минимума
  }
}

console.log("Индекс минимального элемента:", minIndex); // Выводим индекс минимального элемента
